struct FILTER_ {   // стрктура обслуживает кольцевые буфера
    int length;
    int decimateratio;
    int pointer;
    int counter;

    float filter[BufferLen];
    float buffer[BufferLen];
};


//
// This gets used when not optimising
//
float mac(const float *a, const float *b, unsigned int len)
{
 float sum = 0;
 float aa,bb,r;
 unsigned int i;
 for (i = 0; i < len; i++) 
 {
  aa = *a;
  bb = *b;
  r = aa * bb;
  sum = sum + r;
  a++;
  b++;
 }
 return sum;
}




int filter_run(struct FILTER_ *f,float in, float *out)
{
 float *iptr = f->buffer + f->pointer;

 f->pointer++;
 f->counter++;

 *iptr = in;

 if (f->counter == f->decimateratio)
 {
  *out = mac(iptr - f->length, f->filter, f->length);
 }

 if (f->pointer == BufferLen)
 {
  iptr = f->buffer + BufferLen - f->length;
  memcpy(f->buffer, iptr, f->length * sizeof(float));
  f->pointer = f->length;
 }

 if (f->counter == f->decimateratio)
 {
  f->counter = 0;
  return 1;
 }

 return 0;
}

